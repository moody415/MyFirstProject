package Practice;

public class LoopsPrac {
    public static void main(String[] args) {
        int i = 25;
        while (i<=30) {
            System.out.println(i);
            i++;
        }

        int b = 32;
        while (b>35) {
            System.out.println(b);
            b++;
        }


        for(int m=1; m<=25; m++){
            System.out.println(m);
        }







    }
}
